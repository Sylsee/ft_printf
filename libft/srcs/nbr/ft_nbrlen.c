#include "libft.h"

int	ft_nbrlen(long n)
{
	int		count;
	long	nb;

	nb = n;
	count = 0;
	if (nb < 0)
	{
		count++;
		nb *= -1;
	}
	while (nb > 9)
	{
		nb /= 10;
		count++;
	}
	return (++count);
}
